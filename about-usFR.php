								<p>
																		Quelques détails importants :
										<br>
										<strong>1.	Droit commercial </strong>
										<br>
										Le Cabinet assiste et conseille dans les diverses opérations sur les fonds de commerce, négociation et rédaction de tous contrats commerciaux. Les contentieux commerciaux.
										<br>
										<strong>2.	Droit des affaires et des sociétés </strong>  
										<br>
										Le Cabinet assiste dans la Création, fonctionnement et transformation des sociétés commerciales et groupement d’intérêt économique ; fusion, acquisitions et apports d’actifs des sociétés privées, acquisition d’entreprises, négociation et rédaction de pactes d’actionnaires ou d’associés, la joint-venture, etc.
										<br>
										<strong>3.	Droit bancaire</strong>  
										<br>
										Conseil et assistance aux Banques et Etablissements financiers, rédaction des contrats de prêt, mise en place, inscription et renouvellement des sûretés et autres garanties.
										Fort de sa maîtrise du droit bancaire, le Cabinet KIKANGALA & ASSOCIATES assiste ses clients dans la création des Établissements de Crédit, qui sont: les Banques, les Coopératives d'épargne et crédit, les caisses d'épargne, les Institutions Financières spécialisées et les Sociétés Financières. 

Le Cabinet interagit avec la Banque Centrale du Congo pour l'obtention de différents agréments, et obtient toutes les autres autorisations et licences requises par la législation congolaise en la matière.
										<br>
										<strong> 4.	Droit Minier</strong> 
										<br>
										En sa qualité de Mandataire en Mines et Carrières, Maître Didi Kikangala Ebondo représente, conseille et assiste ses clients dans l'octroi et l'exercice des droits miniers et de carrières ainsi que dans le contentieux y afférent. Le Gouvernement congolais l'a reconnu comme justifiant des compétences et connaissances approfondies dans la législation minière ou dans la gestion du domaine des mines et des carrières. Il a rendu ses services honorables dans des entreprises minières et de carrière comme Avocat Conseil. À ce titre, il interagit avec l'Administration des Mines au nom des ses clients, et représente ou assiste ces derniers dans les négociations et conclusions des contrats miniers.    
										<br>
										<strong> 5.	Droit du travail</strong> 
										<br>
										Préparation et rédaction de tous types de contrats de travail, procédure de licenciement économique, négociation, préparation et rédaction de convention de résiliation des contrats de travail de commun accord des parties. Représentation des clients devant toutes les juridictions en matière contentieuse.

										Conseil dans le licenciement collectif (massif) et fermeture d’entreprises ;

										Obtention des visas, permis et cartes de travail pour les étrangers.  
										<br>
										<strong> 6.	Résolution des conflits</strong>

										-	Négociations directes : les parties se rencontrent, discutent et tentent de résoudre le conflit de manière amiable, en recherchant un terrain d’entente par le dialogue et le compromis. 
										<br>	
										-	La conciliation ou la médiation :  négociations sous l’égide d’un tiers indépendant qui va aider les parties à trouver un terrain d’entente.
										<br>
										-	L’arbitrage : régler un conflit non par les tribunaux de l’ordre judiciaire, mais par un ou plusieurs arbitres, choisis et rémunérés par les parties.
										<br>
										<strong> 7.	Le Secrétariat juridique (Company secretary)</strong>

										-	assurer le respect des exigences statutaires et réglementaires ; 
										-	préparer et assurer le suivi de différentes réunions des assemblées générales et du conseil d’administration ; 
										-	veiller à ce que les clientes personnes morales se conforment à la législation et à la règlementation pertinente et tenir les actionnaires, les membres du conseil d’administration et les dirigeants sociaux informés de leurs responsabilités légales.
										-	en tant que représentants désignés de nos clients sur les documents juridiques, assurer que tout fonctionne dans le respect de la loi.
										-	assurer les formalités de dépôt et publicité des actes sociaux, 
										-	assurer la conservation des tous les actes et documents.    

