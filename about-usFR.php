					<h1> Qui sommes nous ? </h1>
					<p> <strong> Le Cabinet Kikangala & Associates est actuellement composé de seize Avocats inscrits au tableau des Barreaux du Haut – Katanga, de Lomami et du Lualaba.
						<br>
						Les Avocats du Cabinet possèdent des connaissances et expertises avérées dans plusieurs domaines de droit : le droit des affaires, le droit civil, le droit privé judiciaire, le droit social, le droit pénal, le droit de l’environnement, le droit administratif, l’arbitrage, etc.
